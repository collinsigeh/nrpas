function doIncrement(maxVaue)
{
  var daysRemaining = document.getElementById('days_remaining');
  var thisValue = daysRemaining.innerHTML;
  while(maxVaue > thisValue)
  {
    thisValue++;
    daysRemaining.innerHTML = thisValue;
  }
}

